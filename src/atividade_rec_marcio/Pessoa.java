package atividade_rec_marcio;

public class Pessoa {
    
    private String nome, endereco, bairro, cidade, estado;
    private int cep;
    
    public Pessoa() {
    }

    public String getNome() {
        return nome;
    }

    public String getEndereco() {
        return endereco;
    }

    public String getBairro() {
        return bairro;
    }

    public String getCidade() {
        return cidade;
    }
    
    public String getEstado() {
        return estado;
    }
    
    public int getCep() {
        return cep;
    }
}
